import React, { Component } from 'react';
import Cell from './Cell.js'
export default class Matrix extends Component {
  
  genRow = (vals) => {
    return vals.map(val => <Cell value={val} />) // rendering a cell component!
  }
  
  genMatrix = () => {
    
    return this.props.values.map(rowVals => <div className="row">{this.genRow(rowVals)}</div>)
    //creating new ROWS that have a className="row"
    //while invoking a function this.genRow(rowVals) that is creating cells
  }
  
  render() {
    return (
      <div id="matrix">
        {this.genMatrix()}
      </div>
    )
  }
  
}
